/**
 * Level maps. Legend:
 *  #  wall          .  floor          ~  water (lava on L6)
 *  B  ?-block       S  spikes         d  decoration
 *  P  warp pipe     K  key chest     C  coin
 *  H  heart         @  hero spawn
 *  E  goomba        T  turtle        F  piranha plant   D  boss
 *
 * Maps are written as INNER rows (without borders). `buildMap` pads every row
 * to the widest one and wraps the whole map in walls, so maps can never be
 * jagged or leak the camera.
 */

export const MAP_W = 30;
export const MAP_H = 18;

export interface LevelDef {
  map: string[];
  intro: string;
}

function buildMap(inner: string[]): string[] {
  const w = MAP_W - 2;
  const rows = inner.slice(0, MAP_H - 2).map((r) => r.slice(0, w).padEnd(w, '.'));
  while (rows.length < MAP_H - 2) rows.push('.'.repeat(w));
  const border = '#'.repeat(MAP_W);
  return [border, ...rows.map((r) => `#${r}#`), border];
}

